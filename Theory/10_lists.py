# Create a list using []
# It is same as string starting from 0 index
a = [1, 28, 17, 43, 14]

# Print the list using print() function
print(a)

# Access using index using a[0], a[1], a[2]
print(a[0]) #---> 1
print(a[3]) #---> 43
# and so on...

# Change the value of list using
a[0] = 55
print(a)

# We can create a list with items of different types
# List can have any data type
c = [28, "Prince", False, 6.9]
print(c)





